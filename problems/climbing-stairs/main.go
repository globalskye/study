func climbStairs(n int) int {
	if n == 1 {
		return 1
	}
	if n == 2 {
		return 2
	}
	a1 := 1
	a2 := 2
	res := 0
	for i := 3; i <= n; i++ {
		res = a1 + a2
		if i%2 == 1 {
			a1 = res
			continue
		}
		a2 = res
	}
	return res
}

